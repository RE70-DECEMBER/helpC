void add(int n1, int n2) {
	int addNum = n1 + n2;
	printf("%d + %d = %d", n1, n2, addNum);
}

void sub(int n1, int n2) {
	int subNum = n1 - n2;
	printf("%d - %d = %d", n1, n2, subNum);
}

void div(int n1, int n2) {
	int divNum = n1 / n2;
	printf("%d / %d = %d", n1, n2, divNum);
}

void mul(int n1, int n2) {
	int mulNum = n1 * n2;
	printf("%d * %d = %d", n1, n2, mulNum);
}
